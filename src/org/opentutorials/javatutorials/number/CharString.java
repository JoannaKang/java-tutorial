package org.opentutorials.javatutorials.number;

public class CharString {
	public static void main(String[] args) {
		System.out.println("생활코딩"+"입니다");
		// comma escape
		System.out.println("egoing said"+"\"Welcome to the world\"");
		// 줄바꿈 
		System.out.println("egoing said\n"+"\"Welcome to the world\"");
	}
}
